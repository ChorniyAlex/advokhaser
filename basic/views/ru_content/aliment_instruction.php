                <li>на каждой странице в правом нижнем углу следует проставить удостоверяющую надпись «Згідно з оригіналом», поставить свою подпись, указать свои имя и фамилию (при этом фамилия пишется заглавными буквами). Поставить дату.</li>
            </ul>
        </li>
        <li>Подайте заявление (два экземпляра с приложениями) в суд лично, или же направьте его в адрес суда по почте. Если подаете заявление лично - предоставьте свой экземпляр заявления для проставления на нём отметки канцелярии суда о принятии заявления.</li>
