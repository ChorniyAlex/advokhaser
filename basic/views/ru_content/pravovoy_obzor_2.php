        <h2>Решение споров относительно участия в воспитании ребенка и порядке встреч с ребенком того из родителей,
            кто проживает отдельно от ребенка</h2>
        <p class="content-text">Интересы детей защищаются прежде всего, и не только национальным законодательством,
            но и нормами международного права. Следует обратить внимание на положение ч.1 ст.3 Конвенции о правах
            ребенка от 20.11.1989 г. (ратифицирована Украиной 27.02.91 г., дата вступления в силу для Украины
