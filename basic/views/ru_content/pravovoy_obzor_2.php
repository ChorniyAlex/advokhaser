            обеспечению интересов ребенка», а также на положение ч.8 ст.7 Семейного Кодекса Украины: «Регулирование
            семейных отношений должно осуществляться с максимально возможным учетом интересов ребенка».</p>
        <p class="content-text">По нашему мнению именно отмеченные нормы должны прежде всего учитываться судом при
