            обстоятельств, которые привели к возникновению спора между родителями относительно участия в воспитании
            ребенка, служба по делам детей составляет вывод. Участие в воспитании ребенка и в случае необходимости
            порядок свидания с ребенком того из родителей, который проживает отдельно от него, устанавливаются
            решением районной, районной в городах Киеве и Севастополе госадминистрации, исполнительного органа
            городского, районного в городе (в случае образования) совета, сельского, поселкового совета объединенной
            территориальной общины, с учетом вывода службы по делам детей. Во время рассмотрения судом споров между
            родителями относительно воспитания ребенка районная, районная в городах Киеве и Севастополе
            госадминистрация, исполнительный орган городского, районного в городе совета подает суду письменный
            вывод о способах участия одного из родителей в воспитании ребенка, место и время их общения,
            составленное на основании сведений, полученных службой по делам детей в результате проведения беседы с
            родителями, ребенком, родственниками, которые принимают участие в ее воспитании, обследования условий
            обитания ребенка, родителей, а также на основании других документов, которые касаются указанного дела».
        </p>
        <p class="content-text">Согласно ч.2 ст.19 СК Украины «Решение органа опеки и попечительства является
            обязательным к исполнению, если на протяжении десяти дней с момента его вынесения заинтересованное лицо
            не обратилось за защитой своих прав или интересов в суд…».</p>
        <p class="content-text">Ст.159 СК Украины предусмотрено решение судом спора относительно участия в
            воспитании ребенка того из родителей, кто проживает отдельно от него: «если тот из родителей, с кем
            проживает ребенок препятствует тому из родителей, кто проживает отдельно, в общении с ребенком и его
            воспитании, в частности если он уклоняется от выполнения решения органа опеки и попечительства, второй
            из родителей имеет право обратиться в суд с иском об устранении этих препятствий. Суд определяет способы
            участия одного из родителей в воспитании ребенка (периодические или систематические свидания,
            возможность совместного отдыха, посещение ребенком места его проживания и прочее), место и время их
            общения. В отдельных случаях, если это вызвано интересами ребенка, суд может обусловить свидание с
            ребенком в присутствии другого лица. При решении спора относительно участия одного из родителей в
            воспитании ребенка берется во внимание отношение родителей к выполнению своих обязанностей, личное
            расположение ребенка к каждому из них, возраст ребенка, состояние его здоровья и другие обстоятельства,
            имеющие существенное значение, в том числе состояние психического здоровья одного из родителей,
            злоупотребление им алкогольными напитками или наркотическими средствами. По заявлению заинтересованной
            стороны суд может приостановить выполнение решения органа опеки и попечительства до решения спора. В
            случае уклонения от выполнения решения суда лицом, с которым проживает ребенок, суд по заявлению того из
            родителей, кто проживает отдельно, может передать ребенка для проживания с ним. Лицо, которое уклоняется
            от выполнения решения суда, обязано возместить материальный и моральный вред, нанесенный тому из
            родителей, кто проживает отдельно от ребенка».</p>
        <p class="content-text">Отдельно хотелось бы остановиться на ч. 2 ст.155 СК Украины: «родительские права не
            могут осуществляться вопреки интересам ребенка». Казалось бы банальная, немножко заезженная правовая
            норма. Но часто на практике именно соблюдение и понимание этой нормы как судом, так и органом опеки и
            попечительства, а иногда и родителями ребенка, имеет очень важное значение. Ведь бывает, что после
            развода один из родителей не так уж и хочет общения с ребенком или имеет намерения ограничить право
            другого из родителей на общение с ребенком, нанести как можно больше волнений превалирует над здравым
            смыслом и над интересами ребенка. И достаточно часто ни судом, ни работниками органа опеки, и
            попечительства, которые решают судьбу ребенка, не учитываются те обстоятельства, которые имеют
            непосредственное отношение к интересам ребенка, а именно: отношения между прежними супругами. Не нужно
            быть психологом, чтобы понимать, что те отношения, которые наблюдает ребенок между своими родителями (а
            родители не бывают бывшими) налагают отпечаток на всю его будущую жизнь и достаточно часто ребенок
            дублирует поведение, а иногда и судьбу своих родителей. Учет этих обстоятельств имеет непосредственное
            отношение к соблюдению норм ч.2 ст.15 ЗУ «Об охране детства»: «родители, которые проживают отдельно от
            ребенка, обязаны принимать участие в его воспитании и имеют право общаться с ним, если судом признано,
            что такое общение не будет препятствовать нормальному воспитанию ребенка».</p>
        <h3>Подведем итог</h3>
        <p class="content-text">К вопросу участия в воспитании и установления порядка встреч с ребенком того из
            родителей, кто проживает отдельно от ребенка: тот из родителей, кто проживает отдельно от ребенка, имеет
            право обратиться в орган опеки и попечительства с соответствующим заявлением. Если кто-либо из родителей
            не согласен с решением органа опеки и попечительства по этому вопросу, то такое решение может быть
            обжаловано в суде в порядке, установленном законом. Тот из родителей, который проживает отдельно от
            ребенка имеет право обратиться в суд с исковым заявлением об устранении препятствий в общении с ребенком
            и в его воспитании.</p>
