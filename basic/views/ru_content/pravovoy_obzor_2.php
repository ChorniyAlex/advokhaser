            СК Украины «орган опеки и попечительства подает суду письменное заключение (вывод) относительно решения
            спора на основании сведений, полученных в результате обследования условий проживания ребенка, родителей,
            других лиц, которые желают проживать с ребенком, принимать участие в ее воспитании, а также на основании
            других документов, касающихся дела».</p>
        <h3>Рассмотрим вопрос решения спора относительно участия в воспитании ребенка и порядке встреч с ребенком
            того из родителей, кто проживает отдельно от ребенка</h3>
        <p class="content-text">По законодательству Украины родители имеют равные права относительно участия в
            воспитании ребенка. Но на практике потому из родителей, кто проживает отдельно от ребенка реализовать
            это право практически не удается. По большому счету, если ребенок, например, остался проживать с
            матерью, и отец ребенка еще не обращался ни в орган опеки и заботы, ни в суд относительно решения
            вопроса участия в воспитании ребенка и установления порядка встреч с ребенком, то мать ребенка бывает
            вообще не предоставляет возможности для встреч с ребенком. Да, это незаконно, но юридически никакой
            ответственности мать ребенка в этом случае не будет нести, поскольку это не предусмотрено действующим
            законодательством. Но, если решением исполнительного органа городского совета с учетом вывода органа
            опеки и попечительства (службы по делам детей) порядок встреч с ребенком и участия в его воспитании уже
            установлен, то в случае препятствия матерью ребенка таким встречам с ребенком, отец ребенка имеет право
            обратиться в суд с иском об устранении препятствий в общении с ребенком и взыскание материального и
            морального ущерба. Если же не выполняется решение суда, которым решен этот вопрос, то опять же отец
            ребенка может обратиться с иском о взыскании материального и морального ущерба. Более того, если мать
            ребенка уклоняется от выполнения решения суда, суд по заявлению отца, может передать ребенка для
            проживания с ним. Кроме того, согласно действующему законодательству, лицо, которое не выполняет решения
            суда, может быть првлечено к уголовной ответственности.</p>
        <p class="content-text">Ст. 158 СК Украины предусмотрено, что решение спора относительно участия в
            воспитании ребенка того из родителей, кто проживает отдельно от него относится к компетенции органа
            опеки и попечительства: «по заявлению матери, отца ребенка орган опеки и попечительства определяет
            способы участия в воспитании ребенка и общения с ним того из родителей, кто проживает отдельно от него.
            Решение об этом орган опеки и попечительства постановляет на основании изучения условий жизни родителей,
            их отношения к ребенку, других обстоятельств имеющих существенное значение. Решение органа опеки и
            попечительства является обязательным к исполнению. Лицо, которое уклоняется от исполнения решения органа
            опеки и попечительства, обязано возместить материальный и моральный вред, нанесенный тому из родителей,
            кто проживает отдельно от ребенка».</p>
        <p class="content-text">Вопрос деятельности органов опеки и заботы, связанный с защитой прав ребенка
            регламентирован Порядком осуществления органами опеки и заботы деятельности, связанной с защитой прав
            ребенка, утвержденной Постановлением Кабинета Министров Украины №866 от 24.09.2008 года (п. п. 73, 74):
            «В случае возникновения спора между родителями относительно участия в воспитании ребенка один из
            родителей, который проживает отдельно от ребенка, подает в службу по делам детей по месту жительства
            (пребывания) ребенка заявление, копию паспорта, справку с места регистрации (проживания), копию
