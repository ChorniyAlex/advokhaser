            <h4>Увага!</h4>
            <p>Позовна заява про розірвання шлюбу за загальним правилом пред’являються в суд за місцем реєстрації відповідача.</p>
            <p>Ви можете пред’явити позов за місцем своєї реєстрації у разі, якщо з на Вашому утриманні є малолітні або неповнолітні діти, або якщо Ви не можете за станом здоров’я чи з інших поважних причин виїхати до місця проживання відповідача.</p>
