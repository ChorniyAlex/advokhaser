                <li>Пошагово вводьте запитувану інформацію про себе, відповідача та іншу необхідну для складання позовної заяви інформацію.</li>
                <li>Після завершення введення інформації натисніть кнопку «Скласти».
                    Заява буде сформована в PDF-файлі.</li>
                <li>Сплатіть кошти за послугу за допомогою он-лайн банкінгу. Після сплати згенеровану позовну заяву буде автоматично відправлено Вам на вказаний ваш e-mail, а також на Вашому екрані відобразиться сформована позовна заява в PDF-файлі. У правому верхньому куті екрану знаходиться функціонал для збереження файлу у потрібному для Вас місці та для його роздруківки.</li>
                <li>Роздрукуйте заяву в 3-х екземплярах: один для себе, два інших для подання до суду.</li>
                <li>Підпишіть її та поставте дату.</li>
                <li>Зробіть ксерокопії документів, які зазначені у додатку до позовної заяви в двох екземплярах, долучіть їх до кожного екземпляру позовної заяви в порядку, зазначеному у позовній заяві.<br><b>Увага!</b> До основної позовної заяви, що буде подаватися в суд, необхідно додати оригінал свідоцтва про шлюб. Якщо у Вас його немає, то Вам слід звернутися до органу РАЦС з метою оформлення дубліката.</li>
