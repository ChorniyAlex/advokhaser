            <h2>Для складання і подачі позовної заяви до суду про розірвання шлюбу необхідно:</h2>
            <h3>Зібрати такі документи та інформацію:</h3>
            <ul>
                <li>свій паспорт та ідентифікаційний код;</li>
                <li>свідоцтво про шлюб;</li>
                <li>свідоцтво про народження дитини (дітей);</li>
                <li>адреса реєстрації (або фактичного проживання) і номер телефону відповідача (чоловіка, дружини), його (її) ідентифікаційний код (якщо відомий).</li>
            </ul>
            <h3>Необхідно розуміти, що:</h3>
            <p>Позивачем по справі є особа, яка звертається із позовом до суду.</p>
