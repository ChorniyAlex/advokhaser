                        <li>на кожній сторінці у правому нижньому кутку слід проставити засвідчувальний напис «Згідно з оригіналом», поставити свій підпис, вказати своє ім'я та прізвище (при цьому прізвище пишеться великими літерами). Зазначити дату.</li>
                    </ul>
                </li>
